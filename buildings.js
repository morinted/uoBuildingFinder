function construct(code, center, polygon, addresses) {
  var constructor = "";
  constructor += '\nbuildings.add(new Building(\n';
  constructor += '    R.string.name_' + code + ',\n';
  constructor += '    R.string.code_' + code + ',\n';
  constructor += '    Arrays.asList(R.string.address_' +
    code + '0';
  var i = 1, len = addresses.length;
  for (i; i < len; i += 1) {
    constructor += ',\n        R.string.address_' + code + i
  }
  constructor += '),\n';
  constructor += '    R.string.code_' + code + ',\n';
  constructor += '    new LatLng(' + center.lat + ', ' + center.lng + '),\n'
  constructor += '    new PolygonOptions().add(\n' +
      '        new LatLng(' + polygon[0].lat + ', ' +
          polygon[0].lng + ')';
  var j =  1, len2 = polygon.length, c;
  for (j; j < len2; j += 1) {
    c = polygon[j];
    constructor += ',\n        new LatLng(' + c.lat + ', ' +
      c.lng + ')';
  }
  constructor += ')\n));\n';
  count += 1;
  return constructor;
}

function createResources(code, name, addresses, URL) {

  // blg code
  english.push(
      '<string translatable="false" name="' +
        'code_' + code +
        '">\n' +
        code +
      '\n</string>\n'
    );

  // blg name
  english.push(
      '<string name="' +
        'name_' + code +
        '">\n' +
        name.en +
      '\n</string>\n'
    );
  french.push(
      '<string name="' +
        'name_' + code +
        '">\n' +
        name.fr +
      '\n</string>\n'
    );

  // blg addresses
  var i = 0, len = addresses.length, address;

  for (i; i < len; i += 1) {
    address = addresses[i];
    english.push(
        '<string name="' +
          'address_' + code + i +
          '">\n' +
          address.en +
        '\n</string>\n'
      );
    french.push(
        '<string name="' +
          'address_' + code + i +
          '">\n' +
          address.fr +
        '\n</string>\n'
      );
  }

    // blg URL
  english.push(
      '<string name="' +
        'URL_' + code +
        '">\n' +
        URL.en +
      '\n</string>\n'
    );
  french.push(
      '<string name="' +
        'URL_' + code +
        '">\n' +
        URL.fr +
      '\n</string>\n'
    );
}

